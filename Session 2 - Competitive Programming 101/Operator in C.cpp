#include <stdio.h>

//PEMAKAIAN OPERATOR PADA C
int main(){
	int a = 10, b = 7;
	printf("Ini hasil dari a + b = %d\n", a + b);
	printf("Ini hasil dari a - b = %d\n", a - b);
	printf("Ini hasil dari a * b = %d\n", a * b);
	printf("Ini hasil dari a / b = %d\n", a / b);
	printf("Ini hasil dari a modulus b = %d\n", a % b);
	
	int x = 10, y = 35;
	printf("Operasi Irrasional pada C\n");
	printf("Apakah x > y? %d\n", x > y); // 0 itu False sementara 1 itu True
	printf("Apakah x < y? %d\n", x < y);
	return 0;
}
